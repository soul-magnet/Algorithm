package DynamicProgramming;

import java.util.ArrayList;

/*
 * Given an array of integers, find two non-overlapping subarrays 
 * which have the largest sum.The number in each subarray should be contiguous.
 * Return the largest sum.
 * For given [1, 3, -1, 2, -1, 2], the two subarrays are [1, 3] and [2, -1, 2] 
 * or [1, 3, -1, 2] and [2], they both have the largest sum 7.
 * Note: The subarray should contain at least one number
 * Challenge: Can you do it in time complexity O(n) ?
 * */
public class MaximumSubarrayII {
	/**
     * @param nums: A list of integers
     * @return: An integer denotes the sum of max two non-overlapping subarrays
     */
	public int maxTwoSubArrays(ArrayList<Integer> nums){
		int size = nums.size();
		int[] left = new int[size];
		int[] right = new int[size];
		int sum = 0;
		int minSum = 0;
		// left to right
		int max = Integer.MIN_VALUE;
		for (int i = 0; i < size; i++){
			sum += nums.get(i);
			max = Math.max(max, sum - minSum);
			minSum = Math.min(sum, minSum);
			left[i] = max;
		}
		// right to left
		sum = 0;
		minSum = 0;
		max = Integer.MIN_VALUE;
		for (int i = size - 1; i >= 0; i--){
			sum += nums.get(i);
			max = Math.max(max, sum -minSum);
			minSum = Math.min(sum, minSum);
			right[i] = max;
		}
		// merge
		max = Integer.MIN_VALUE;
		for (int i = 0; i < size - 1; i++){
			max = Math.max(max, left[i] + right[i + 1]);
		}
		return max;
	}
}
